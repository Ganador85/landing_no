async function columnExists(column) {
  const res = await pool.query(
    `select 1 from information_schema.columns
     where table_schema='public' and table_name='site_settings' and column_name=$1`,
    [column],
  );
  return (res.rowCount ?? 0) > 0;
}

async function ensureColumn(column) {
  if (await columnExists(column)) {
    console.log(`ok: ${column}`);
    return;
  }
  await pool.query(`alter table site_settings add column "${column}" varchar`);
  console.log(`added: ${column}`);
}

try {
  for (const [prefix, fields] of Object.entries(groups)) {
    for (const field of fields) {
      await ensureColumn(`${prefix}_${field}`);
    }
  }
  console.log("Page copy schema ready.");
} catch (err) {
  console.error(err);
  process.exitCode = 1;
} finally {
  await pool.end();
}
